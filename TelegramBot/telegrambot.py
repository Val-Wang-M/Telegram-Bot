def translate_to_emoji(word: str):
    # Check for the word in the dictionary (singular or plural)
    if word in emoji_dict:
        return emoji_dict[word]

    # Handle plural words (by removing the trailing 's')
    if word.endswith('s'):
        singular_word = word[:-1]  # Remove 's' from the end
        if singular_word in emoji_dict:
            return emoji_dict[singular_word]

    # If no match is found, return the original word
    return word


async def emoji_lang(update: Update, context: CallbackContext):
    if not context.args:  # If user didn't provide any text
        await update.message.reply_text("Please provide a sentence! Example: /emoji_lang I love dogs")
        return

    words = " ".join(context.args).lower().split()  # Convert input to lowercase and split into words
    translated_sentence = []

    for word in words:
        # Use the translate_to_emoji function for each word
        translated_sentence.append(translate_to_emoji(word))

    response = " ".join(translated_sentence)  # Join words back into a sentence
    await update.message.reply_text(f"Translated: {response}")


